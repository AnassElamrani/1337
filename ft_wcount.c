#include "libft.h"

int		ft_wcount(char const *s, char c)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s[i])
	{
		while (s[i] == c && s[i])
			i++;
		if (s[i] && s[i] != c)
			j++;
		while (s[i] != c && s[i])
			i++;
	}
	return (j);
}
