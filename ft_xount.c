#include "libft.h"

int		ft_xount(int nb)
{
	int x;

	x = 1;
	if (nb < 0)
	{
		nb = -nb;
		x = 2;
	}
	if (x == 0)
		return (x = 0);
	while (nb > 9)
	{
		nb = nb / 10;
		x++;
	}
	return (x);
}
